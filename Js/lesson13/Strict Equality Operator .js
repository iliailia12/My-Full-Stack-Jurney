// Strict Equality Operator (===) JavaScript-ში
// Strict Equality Operator (===) გამოიყენება, რათა შედარება განხორციელდეს მნიშვნელობისა და ტიპის მიხედვით.

// === უბრუნებს true მხოლოდ მაშინ, როდესაც ორივე მნიშვნელობა და ტიპი ემთხვევა.

// == განსხვავებით, === არ ატარებს ტიპის ავტომატურ კონვერსიას.


let a = 5;       
let b = "5";   

console.log(a === b); 
console.log(a === 5); 
