// JavaScript array.reduce()

// JavaScript-ის reduce მეთოდი გამოიყენება მასივის ყველა ელემენტის ერთიან მნიშვნელობად დასამუშავებლად, მაგალითად, ჯამად, პროდუქტად ან კომპლექსურ ობიექტად. reduce ხშირად გამოიყენება ფუნქციურ პროგრამირებაში.

// აქ არის მარტივი მაგალითი:

const numbers = [1, 2, 3, 4, 5];
const sum = numbers.reduce((total, number) => total + number, 0);
console.log(sum);
 

// reduce იღებს callback ფუნქციას და საწყის მნიშვნელობას.

// Callback ფუნქციის არგუმენტები:

// total (გამოცხადებული კუმულატიური მნიშვნელობა),

// number (მიმდინარე ელემენტი მასივში).

// ფუნქცია თითოეული ელემენტისთვის ითვლის ახალ total მნიშვნელობას და ბოლოს აბრუნებს შედეგს.

// საწყისი მნიშვნელობა (ამ მაგალითში 0) საჭიროა რათა მნიშვნელობები სწორად დაჯამდეს.