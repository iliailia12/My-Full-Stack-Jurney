// არითმეტიკული გამონათქვამები JavaScript-ში
// JavaScript-ში არითმეტიკული გამონათქვამები გამოიყენება რიცხვებით მათემატიკური ოპერაციების შესასრულებლად. ძირითადი ოპერატორებია:

// + (მოკრება): ორი რიცხვის ან მნიშვნელობის დამატება.

// - (გამოკლება): ერთი რიცხვის ან მნიშვნელობის გამოკლება მეორედან.

// * (გამრავლება): რიცხვების გამრავლება.

// / (განაწილება): რიცხვის გაყოფა.

// % (მოდულო): დანარჩენი გაყოფის შემდეგ.


// 1. მოკრება (+)
let sum = 10 + 5; 

// 2. გამოკლება (-)
let difference = 20 - 8;

// 3. გამრავლება (*)
let product = 6 * 4; 

// 4. გაყოფა (/)
let quotient = 20 / 4; 

// 5. მოდულო (%)
let remainder = 17 % 3; 

// 6. კომბინირებული ოპერაცია
let result = (10 + 5) * 2 - 3; 
