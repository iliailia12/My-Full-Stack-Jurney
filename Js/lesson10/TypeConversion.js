// Type Conversion in JavaScript
// Type conversion არის მონაცემების ერთ ტიპიდან მეორეში გარდაქმნის პროცესი. JavaScript-ში ეს შეიძლება იყოს:

// Implicit Conversion (ავტომატური): JavaScript ავტომატურად გარდაქმნის მონაცემს საჭირო ტიპში.

// მაგალითად: "5" + 2 -> "52" (String), "5" * 2 -> 10 (Number).

// Explicit Conversion (მხოლოდ ხელით): მონაცემის ტიპის პირდაპირი გადაყვანა.

// String Conversion: String(123) -> "123".

// Number Conversion: Number("456") -> 456.

// Boolean Conversion: Boolean(0) -> false, Boolean("text") -> true.




// JavaScript ავტომატურად გარდაქმნის მონაცემს საჭირო ტიპში:
let result = "5" + 3;  
let num = "10" - 2;   

// მონაცემების ტიპი პირდაპირ შეიძლება შეიცვალოს:

// String Conversion
let str = String(25);  

// Number Conversion
let num1 = Number("123");  

// Boolean Conversion
let isTrue = Boolean("Hello");  
let isFalse = Boolean(0);  