// JavaScript function expressions
// JavaScript-ის ფუნქციის გამოსახულებები (Function Expressions) საშუალებას გაძლევთ ფუნქცია ცვლადში განსაზღვროთ. ფუნქციის გამოსახულება არ არის სახელიანი (ანონიმური), თუმცა შეგიძლიათ სახელიც მიანიჭოთ.

 
const greet = function(name) {
  console.log(`გამარჯობა, ${name}!`);
};

greet("ნინო");
 

// function(name) არის ფუნქციის გამოსახულება.

// ის ცვლადში (greet) ინახება.

// ფუნქციის გამოსახულება შეიძლება გადაეცეს სხვა ფუნქციას ან გამოიძახოს პირდაპირ.