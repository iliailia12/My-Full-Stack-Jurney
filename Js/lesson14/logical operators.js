// ლოგიკური ოპერატორები JavaScript-ში: && (AND) და || (OR)
// && (AND): იყენებს ორი პირობის შესამოწმებლად. უბრუნებს true მხოლოდ მაშინ, როცა ორივე პირობა მართალია. || (OR): იყენებს ორი პირობის შესამოწმებლად. უბრუნებს true თუ მინიმუმ ერთი პირობა მართალია.


let age = 25;
let isMember = true;

if (age > 18 && isMember) {
  console.log("თქვენ გაქვთ წვდომა!");
}

if (age < 18 || !isMember) {
  console.log("წვდომა არ გექნებათ.");
}
