// Math ობიექტის მეთოდები JavaScript-ში
// JavaScript-ში Math ობიექტი შეიცავს სხვადასხვა მეთოდებს და თვისებებს, რომლებიც გამოიყენება მათემატიკური გამოთვლებისთვის. იგი არ საჭიროებს შექმნას, რადგან არის ჩაშენებული ობიექტი.

// ძირითადი Math მეთოდები
// Math.round() მრგვალებს რიცხვს უახლოეს მთელ რიცხვამდე.

// მაგალითი: Math.round(4.6) -> 5

// Math.ceil() მრგვალებს რიცხვს ზევით უახლოეს მთელ რიცხვამდე.

// მაგალითი: Math.ceil(4.2) -> 5

// Math.floor() მრგვალებს რიცხვს ქვემოთ უახლოეს მთელ რიცხვამდე.

// მაგალითი: Math.floor(4.8) -> 4

// Math.max() აბრუნებს გადაცემული რიცხვებიდან მაქსიმუმს.

// მაგალითი: Math.max(10, 20, 5) -> 20

// Math.min() აბრუნებს გადაცემული რიცხვებიდან მინიმუმს.

// მაგალითი: Math.min(10, 20, 5) -> 5

// Math.sqrt() აბრუნებს რიცხვის კვადრატულ ფესვს.

// მაგალითი: Math.sqrt(16) -> 4

// Math.pow() რიცხვის ხარისხში აყვანა.

// მაგალითი: Math.pow(2, 3) -> 8 (2³)

// Math.random() აბრუნებს შემთხვევით რიცხვს 0-სა და 1-ს შორის.

// მაგალითი: Math.random() -> მაგალითად: 0.534

// Math.abs() აბრუნებს რიცხვის აბსოლუტურ მნიშვნელობას.

// მაგალითი: Math.abs(-5) -> 5

// Math.trunc() შლის რიცხვის ათწილად ნაწილს და აბრუნებს მხოლოდ მთელ ნაწილს.

// მაგალითი: Math.trunc(4.9) -> 4

// Math ობიექტის თვისებები
// Math.PI: პი (π) კონსტანტა (≈ 3.14159).

// მაგალითი: Math.PI -> 3.14159

// Math.E: ეილერის რიცხვი (≈ 2.718).

// მაგალითი: Math.E -> 2.718





// Math.round() - რიცხვის მოჭრა უახლოეს მთელ რიცხვამდე
let rounded = Math.round(4.6); // შედეგი: 5

// Math.ceil() - რიცხვის ზემოთ მოჭრა უახლოეს მთელ რიცხვამდე
let ceiling = Math.ceil(4.2); // შედეგი: 5

// Math.floor() - რიცხვის ქვემოთ მოჭრა უახლოეს მთელ რიცხვამდე
let floored = Math.floor(4.8); // შედეგი: 4

// Math.max() - მაქსიმალური მნიშვნელობა სიიდან
let maximum = Math.max(10, 20, 5); // შედეგი: 20

// Math.min() - მინიმალური მნიშვნელობა სიიდან
let minimum = Math.min(10, 20, 5); // შედეგი: 5

// Math.sqrt() - კვადრატული ფესვის გამოთვლა
let squareRoot = Math.sqrt(16); // შედეგი: 4

// Math.pow() - რიცხვის ხარისხში აყვანა
let power = Math.pow(2, 3); 

// Math.random() - შემთხვევითი რიცხვის გენერირება (0-დან 1-მდე)
let random = Math.random(); 

// Math.abs() - რიცხვის აბსოლუტური მნიშვნელობა
let absolute = Math.abs(-5); 

// Math.trunc() - რიცხვის ათწილად ნაწილის მოშლა
let truncated = Math.trunc(4.9);
