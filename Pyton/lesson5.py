# პითონი მხარს უჭერს ყველა სტანდარტულ არითმეტიკულ ოპერაციას, როგორიცაა შეკრება, გამოკლება, გამრავლება, გაყოფა და სხვა.

# დამატება
sum = 5 + 3
print( sum)

#  გამოკლება
difference = 5 - 3
print(difference)

#  გამრავლება
product = 5 * 3
print(product)

# #განყოფილება
quotient = 5 / 3
print(quotient)

#  სართულის განყოფილება (აბრუნებს გაყოფის მთელ ნაწილს)
floor_division = 5 // 3
print( floor_division)

# მოდული (აბრუნებს დანარჩენ განყოფილებას)
remainder = 5 % 3
print(remainder)


# გაძლიერება (ამაღლებს 5-ს 3-ის ხარისხზე)
power = 5 ** 3
print(power)

