# Format specifiers in Python 

# Python-рЃерЃў рЃцрЃЮрЃарЃЏрЃљрЃбрЃўрЃА рЃАрЃърЃћрЃфрЃўрЃцрЃўрЃЎрЃљрЃбрЃЮрЃарЃћрЃЉрЃў (format specifiers) рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃћрЃЉрЃўрЃА рЃцрЃЮрЃарЃЏрЃљрЃбрЃўрЃарЃћрЃЉрЃўрЃАрЃЌрЃЋрЃўрЃА, рЃњрЃљрЃюрЃАрЃљрЃЎрЃБрЃЌрЃарЃћрЃЉрЃўрЃЌ рЃЏрЃљрЃерЃўрЃю, рЃарЃЮрЃфрЃљ рЃњрЃЋрЃАрЃБрЃарЃА рЃфрЃЋрЃџрЃљрЃЊрЃўрЃА рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃўрЃА рЃЕрЃљрЃАрЃЏрЃљ рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃерЃў рЃњрЃљрЃюрЃАрЃљрЃќрЃдрЃЋрЃарЃБрЃџрЃў рЃцрЃЮрЃарЃЏрЃљрЃбрЃўрЃЌ.

# рЃЦрЃЋрЃћрЃЏрЃЮрЃЌ рЃЕрЃљрЃЏрЃЮрЃЌрЃЋрЃџрЃўрЃџрЃўрЃљ рЃќрЃЮрЃњрЃљрЃЊрЃў рЃцрЃЮрЃарЃЏрЃљрЃбрЃўрЃА рЃАрЃърЃћрЃфрЃўрЃцрЃўрЃЎрЃљрЃбрЃЮрЃарЃћрЃЉрЃў рЃЊрЃљ рЃЏрЃљрЃЌрЃў рЃљрЃдрЃгрЃћрЃарЃљ рЃЦрЃљрЃарЃЌрЃБрЃџрЃљрЃЊ:

# ­Ъћ╣ рЃФрЃЋрЃћрЃџрЃў рЃАрЃбрЃўрЃџрЃў (% рЃЮрЃърЃћрЃарЃљрЃбрЃЮрЃарЃўрЃЌ)
 
# name = "рЃюрЃўрЃюрЃЮ"
# age = 25
# print("рЃЏрЃћ рЃЏрЃЦрЃЋрЃўрЃљ %s рЃЊрЃљ рЃЋрЃљрЃа %d рЃгрЃџрЃўрЃА." % (name, age))
# рЃФрЃўрЃарЃўрЃЌрЃљрЃЊрЃў рЃАрЃърЃћрЃфрЃўрЃцрЃўрЃЎрЃљрЃбрЃЮрЃарЃћрЃЉрЃў:
# рЃАрЃърЃћрЃфрЃўрЃцрЃўрЃЎрЃљрЃбрЃЮрЃарЃў	рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ
# %s	рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃў
# %d	рЃЏрЃЌрЃћрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў
# %f	рЃљрЃЌрЃгрЃўрЃџрЃљрЃЊрЃў рЃарЃўрЃфрЃ«рЃЋрЃў
# %.2f	рЃљрЃЌрЃгрЃўрЃџрЃљрЃЊрЃў 2 рЃфрЃўрЃцрЃарЃўрЃЌ рЃгрЃћрЃарЃбрЃўрЃџрЃўрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ
# %x	рЃЌрЃћрЃЦрЃЋрЃАрЃЏрЃћрЃбрЃЮрЃЉрЃўрЃЌрЃў рЃцрЃЮрЃарЃЏрЃљ (lowercase)
# %o	рЃарЃЋрЃљрЃЮрЃЉрЃўрЃЌрЃў рЃцрЃЮрЃарЃЏрЃљ
# ­Ъћ╣ рЃљрЃ«рЃљрЃџрЃў рЃАрЃбрЃўрЃџрЃў (str.format() рЃЏрЃћрЃЌрЃЮрЃЊрЃўрЃЌ)
 
# print("рЃЏрЃћ рЃЏрЃЦрЃЋрЃўрЃљ {} рЃЊрЃљ рЃЋрЃљрЃа {} рЃгрЃџрЃўрЃА.".format("рЃњрЃўрЃЮрЃарЃњрЃў", 30))
# рЃерЃћрЃњрЃўрЃФрЃџрЃўрЃљ рЃЏрЃўрЃБрЃЌрЃўрЃЌрЃЮ рЃърЃЮрЃќрЃўрЃфрЃўрЃљ рЃљрЃю рЃбрЃўрЃърЃў:

 
# print("рЃЏрЃљрЃЌрЃћрЃЏрЃљрЃбрЃўрЃЎрЃљ: {:.2f}".format(95.456))  # рЃЮрЃа рЃфрЃўрЃцрЃарЃљрЃЏрЃЊрЃћ
# ­Ъћ╣ f-рЃАрЃбрЃарЃўрЃюрЃњрЃћрЃЉрЃў (Python 3.6+)
 
# name = "рЃџрЃћрЃЋрЃљрЃюрЃў"
# age = 28
# print(f"рЃЏрЃћ рЃЏрЃЦрЃЋрЃўрЃљ {name} рЃЊрЃљ рЃЋрЃљрЃа {age} рЃгрЃџрЃўрЃА.")
# f-рЃАрЃбрЃарЃўрЃюрЃњрЃўрЃА рЃАрЃърЃћрЃфрЃўрЃцрЃўрЃЎрЃљрЃбрЃЮрЃарЃћрЃЉрЃў:
# рЃцрЃЮрЃарЃЏрЃљрЃбрЃў	рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў	рЃерЃћрЃЊрЃћрЃњрЃў
# {value:.2f}	f"{3.14159:.2f}"	"3.14"
# {value:>10}	f"{'abc':>10}"	" abc" (рЃЏрЃљрЃарЃ»рЃЋрЃћрЃюрЃљ рЃърЃљрЃЊрЃўрЃюрЃњрЃў)
# {value:<10}	f"{'abc':<10}"	"abc " (рЃЏрЃљрЃарЃфрЃ«рЃћрЃюрЃљ рЃърЃљрЃЊрЃўрЃюрЃњрЃў)
# {value:^10}	f"{'abc':^10}"	" abc " (рЃфрЃћрЃюрЃбрЃарЃўрЃарЃћрЃЉрЃБрЃџрЃў)