# String methods in Python

# Python-ში სტრინგის მეთოდები საშუალებას გაძლევთ ეფექტურად მართოთ და შეცვალოთ ტექსტური მონაცემები. აი, ძირითადი მეთოდები:

# ძირითადი სტრინგის მეთოდები:
# capitalize(): ტექსტის პირველი სიმბოლოს გადაქცევა დიდ ასოდ.

 
txt ="გამარჯობა"
print(txt.capitalize())   
# lower() და upper(): ყველა სიმბოლოს გადაქცევა პატარა ან დიდი ასოებად.

 
txt ="Python"
print(txt.lower())   
print(txt.upper())  
# strip(): txtს დასაწყისში და ბოლოში არსებული ცარიელი ადგილების მოცილება.

 
txt= "  გამარჯობა  "
print(txt.strip())   
# replace(): ტექსტში ერთი ნაწილის შეცვლა მეორეთი.

 
txt= "მე მიყვარს Python"
print(txt.replace("Python", "კოდირება"))  
# split(): txtს გაყოფა სიად (list) კონკრეტული გამყოფით.

 
txt ="ვაშლი,ბანანი,ალუბალი"
print(txt.split(","))   
# დამატებითი სტრინგის მეთოდები:
# find(): ტექსტში კონკრეტული ნაწილის პირველი მდებარეობის პოვნა.

 
txt ="გამარჯობა მსოფლიო"
print(txt.find("მსოფლიო"))  
# join(): სიაში არსებული ელემენტების გაერთიანება ერთ ტექსტში.

 
სიტყვები = ["Python", "არის", "სასიამოვნო"]
print(" ".join(სიტყვები))  
# startswith() და endswith(): ამოწმებს, იწყება თუ მთავრდება txt კონკრეტული ნაწილით.

 
txt ="გამარჯობა მსოფლიო"
print(txt.startswith("გამარჯობა"))  
print(txt.endswith("მსოფლიო"))     
# count(): ტექსტში კონკრეტული ნაწილის რაოდენობის დათვლა.

 
txt ="ბანანი"
print(txt.count("ა"))   
# isalpha(), isdigit(), isalnum(): ამოწმებს, შეიცავს თუ არა txt მხოლოდ ასოებს, რიცხვებს ან ორივეს.

 
txt= "Python3"
print(txt.isalpha())   
print(txt.isdigit())   
print(txt.isalnum())   