# Python-ში სტრინგის ინდექსირება საშუალებას გაძლევთ ტექსტში კონკრეტული სიმბოლოების წვდომა მათი პოზიციის, ანუ ინდექსის, გამოყენებით. აი, როგორ მუშაობს:
 
# პოზიტიური ინდექსირება: იწყება 0-დან პირველი სიმბოლოსთვის, 1-დან მეორე სიმბოლოსთვის და ა.შ.
 
txt = "Python"
print(txt[0])  
print(txt[3])  
# ნეგატიური ინდექსირება: იწყება -1-დან ბოლო სიმბოლოსთვის, -2-დან ბოლო წინა სიმბოლოსთვის და ა.შ.
 
txt = "Python"
print(txt[-1])   
print(txt[-3])   
# სლაისინგი (Slicing): txtს ნაწილის ამოღება ინდექსების დიაპაზონის გამოყენებით.
 
txt = "Python"
print(txt[0:3])   
print(txt[2:])    
print(txt[:4])    
